Session 4 - Session 6
- Current Board members will train the new Board members so they are ready to take over at the end of Session 6. Each current Board member should reach out to their replacement to set up a plan. Some positions will require more time, so each position can determine how to coordinate.


Instructions

How to add a new member
NOTE: New members should not be added until dues are paid in full.
1. Login to the Google Admin Website.
2. Select "Add a user".
3. Enter the new member's first and last name.
4. Primary Email: Assign a new email address for each participating adult (typical method is first name and the first initial of the last name — if this results in a duplicate, decide how to modify the new member's email at your discretion).
5. Secondary Email: Use the email address that they used to register with.
6. The phone number field is nice to have but not necessary.
7. It will send the information to the Secondary Email with a temporary password. They will have 7 days to login and change their password.
8. Add their new R&W account to the group Current Members.
9. Email their secondary email address letting them know to look for the message with account information. If it expires, you will need to reset their password.
   Welcome email template: https://docs.google.com/document/d/1uEaNusn6qA9D-GzwMNgFi3A8i9shEFGMfzO23cZ1lEw/edit?usp=sharing
10. It's helpful to text the new member and let them know to look for the email.
11. A Liability Waiver should also be filled out and returned. (Waiver Request email template.)

How to delete Google accounts for non-returning members
1. Reach out to the individuals prior to deleting the account to verify that they won't be returning to Roots & Wings.
2. Give them some time to remove any information they have from these accounts before deleting them.
3. Log in to Google Admin.
4. Users — or type their name into search.
5. Delete a user.
6. Assign Drive files of the deleted user to communications@rootsandwingsindy.com.

How to make a survey
1. Go to Forms.
2. Select a blank template.
3. Give the survey a title.
4. You can add a description.
5. Add the question(s).
6. Verify the settings. Typically surveys are anonymous. You can decide whether members will have an opportunity to edit their response or not.
NOTES:
- Give the surveys a minimum of 1 week for responses.
- Let the Board know what the response is prior to announcing to all members.
- Make sure to close the survey for responses at the end of the time period.
- Send out reminders as necessary for people to respond.

How to reset passwords
1. Log in to Google Admin.
2. Select Users.
3. Scroll to the member's name. It will highlight options on the right. Select reset password. It will send a new temporary password to the member at a secondary email address. They will have 7 days to login and change their password.

How to update groups
1. Log in to Google Admin.
2. Select Users → Groups.
3. Select Manage Members for the group you want to edit.
4. Add/delete email addresses as necessary.

How to update the Roots & Wings Google Site
1. Click on the website to update.
2. It will pop up the website in an editor. You can click on the different links at the top to view those pages and edit them.
3. You can drag and drop new blocks or edit the existing blocks. No changes will be visible on the website until you publish it.
4. Click Publish when you're done editing. It will show you the previous version and the new version. If you're happy with the results, you can click Publish again to upload those changes.


Troubleshooting

- New members may need their password reset if they don't login before it expires.
- Sometimes when board accounts are switched over, they are unable to login. Communications will get an email saying there was suspicious activity on the account. The solution is to go into the user account and click "Turn off identity questions for 10 minutes." The user will then have 10 minutes to sign in. The account should work after.


