Roots and Wings Homeschool
Communications Director

Job Length:
2 year commitment


Overview:
The Communications Director is the Chair of the Communications Committee and in charge of the internal communications of the organization.

The majority of the Communications Director responsibilities involve maintaining the Roots & Wings Handbook, gathering signed Membership Agreement/Liability Forms, maintaining the Binder of printed documents, managing the Roots & Wings Google Workspace and creating and executing Surveys. During the second year of their term, the Communications Director will need to coordinate the election of the new Board.

Major Job Duties:
- Member of the Board.
- Head of the Communications Committee.
- Ensures that all essential co-op documents as well as the webpage and GSuite maintains a second owner/admin/editor at all times. Typically this should be president@rootsandwingsindy.com
- Works closely with Board Members to ensure clear internal communications.
- Maintains communications on all volunteer/job lists. Make sure they are posted and up to date. Oversees the process of making sure these are updated each year.
- Helps choose and maintain classroom communication consistency.
- Maintains website on accuracy and currency.
- Update roots and wings website as needed.
- Updates classes as sessions change.
- Posts information from members.
- Updates community calendar.
- Post co-op links to forms.
- Make any updates to job descriptions as needed throughout the year.
- Get signatures for all waivers needed from each member, each year.
- Evaluate, update, and change Job description at the end of your 2 year term. In small font in the upper right hand corner of the document put your first initial, last name, and the date of the edit/review. Do the same for any job descriptions that fall under your committee that are not updated by the outgoing volunteer.
- Actively work to find a qualified candidate for your role prior to the end of your term.
- Ensure that outgoing volunteers on your committee update their job descriptions.


